// 1) What is Object-Oriented Programming (OOP)?
// Object-Oriented Programming (OOP) is a programming paradigm that organizes software design around objects, rather than just functions and logic.

// An object is a real-world entity that has:

// State (Attributes / Data members) → variables that hold data.

// Behavior (Methods / Functions) → operations that can be performed on data.

// The main principle of OOP is to bundle data and the methods that operate on that data into a single unit called an object, and create reusable software components.

// OOP provides several powerful concepts like:

// Encapsulation (wrapping data and methods together)

// Abstraction (hiding implementation details)

// Inheritance (reusing and extending existing classes)

// Polymorphism (same function behaving differently in different contexts)

// Examples of OOP languages: Java, C++, Python, C#, Ruby, etc.

// 2) What is Procedure-Oriented Programming (POP)?

// Procedure-Oriented Programming (POP) is a programming paradigm based on the concept of functions or procedures.

// In POP, the main focus is on procedures (functions), which operate on data. Data and functions are treated as separate entities, and the program is usually written as a sequence of functions that manipulate the data.

// Examples of POP languages: C, Pascal, Fortran, BASIC, etc.